package Variable;

public class IntegerDemo {
    public static void main(String[] args) {
        int a;
        a = 1;
        System.out.println(a);

        int b;
        b = 2;
        System.out.println(b);

        int result = a*b;
        System.out.println(result);

        double c;
        c = 1.5;
        System.out.println(c);

        String d;
        d = "배진우";
        System.out.println(d);

    }
}
